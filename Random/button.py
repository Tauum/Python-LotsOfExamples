class Button:
    def __init__(self, x, y, w=100, h=50):
        self.x = x
        self.y = y
        self.width = w
        self.height = h
        self.colour = (0, 0, 255)

def moveLeft(self):
    self.x = self.x - 10
def moveRight(self):
    self.x = self.x + 10
def moveUp(self):
    self.y = self.y - 10
def moveDown(self):
    self.y = self.y + 10
